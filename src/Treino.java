public class Treino {
    private String modalidade;
    private String professor;
    private AtividadeFisica atividadeFisica;

    /**
     * @return the modalidade
     */
    public String getModalidade() {
        return modalidade;
    }

    /**
     * @param modalidade the modalidade to set
     */
    public void setModalidade(String modalidade) {
        this.modalidade = modalidade;
    }

    /**
     * @return the professor
     */
    public String getProfessor() {
        return professor;
    }

    /**
     * @param professor the professor to set
     */
    public void setProfessor(String professor) {
        this.professor = professor;
    }

    /**
     * @return the atividadeFisica
     */
    public AtividadeFisica getAtividadeFisica() {
        return atividadeFisica;
    }

    /**
     * @param atividadeFisica the atividadeFisica to set
     */
    public void setAtividadeFisica(AtividadeFisica atividadeFisica) {
        this.atividadeFisica = atividadeFisica;
    }
}
